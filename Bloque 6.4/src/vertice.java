
public class vertice {
private int x,y;

public vertice(int x, int y){ //constructor
	this.x = x;
	this.y = y;
	
}
// Set and Get
public int getX() {
	return x;
}

public void setX(int x) {
	this.x = x;
}

public int getY() {
	return y;
}

public void setY(int y) {
	this.y = y;
}


}
