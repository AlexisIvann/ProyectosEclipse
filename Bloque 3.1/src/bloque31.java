
public class bloque31 {
	public static void main(String[] arg){
		int i = 0;
		int j;
	
		while(i < 5){
			System.out.println(i+" ");
			i += 1;
			
		}
		System.out.println("Estoy fuera del bucle");
		
		for(j = 0; j < 5; j += 1){
			System.out.println(j+" ");
			
		}
		System.out.println("Estoy fuera del bucle for");
	}

}
